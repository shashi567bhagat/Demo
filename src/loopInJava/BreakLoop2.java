package loopInJava;

public class BreakLoop2 {

	
	
	public static void main(String[] args) {
		
		int i;
		int j;
		
		for(i=1;i<=10;i++) {
			
			for(j=1;j<=5;j++) {
				
			if(i==2 && j==4) {
				
				
				break ;
			}	
				
			System.out.println(i +"-------->"+ j);	
				
			}
			
			
			
			
			
			
			
			
			
		}
		
		
		
	}
}
