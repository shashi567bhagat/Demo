package loopInJava;

public class ForLoop2 {
	
	
	public static void main(String[] args) {
		
		int i;
		int j;
		
		for(i=1;i<=5;i++) {
			
			
			for(j=1;j<5;j++) {
				
				System.out.println(i + "----->" + j);
				
			  }
			
		System.out.println("------------------------------");
			
		}
			
			
			
			
		}
		
		
		
	}


