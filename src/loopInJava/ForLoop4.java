package loopInJava;

public class ForLoop4 {
	
	
	public static void main(String[] args) {
		
		int i;
		int j;
		
	
		for(i=1;   i<=10;  i++) {
			
			for(j=1;  j<=i;  j++) {
				
				
				System.out.print("   " );
				
				System.out.print("*");
				
				
			}
			
			System.out.println();
			
			
		}
		
		
		
	}

}
