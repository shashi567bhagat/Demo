package operators;

public class TernaryOperator2 {

	
	public static void main(String[] args) {
		
		int a=10;
		int b=4;
		
		if(a>b) {
			
			System.out.println(true);
		}
		else
			System.out.println(false);
		
		// or
		
		System.out.println(a>b? true:false);  //The ternary operator (conditional operator) is shorthand for the if-then-else statement.
		
	}
	
	
}
