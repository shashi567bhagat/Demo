package string;

public class Value_Of_Boolean_to_String {

	public static void main(String[] args) {          
        // Boolean to String  
        boolean bol = true;    
        boolean bol2 = false;    
        String s1 = String.valueOf(bol);    
        String s2 = String.valueOf(bol2);  
        System.out.println(s1+50);  //concate
        System.out.println(s2);  
    }  
	
	
	
}
