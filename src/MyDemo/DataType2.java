package MyDemo;

public class DataType2 {
	
	//Video No -6
	
	public static void main(String[] args) {
		
		//byte
		
		byte b=127;
		byte MIN_VALUE1=Byte.MIN_VALUE;
		byte MAX_VALUE1=Byte.MAX_VALUE;
		System.out.println(b);
		System.out.println("minValue :- " +MIN_VALUE1 + "     "+  "  maxvalue:- "        + MAX_VALUE1   );
		  
		//short
		
		short s=127;
		short MIN_VALUE2=Short.MIN_VALUE;
		short MAX_VALUE2=Short.MAX_VALUE;
		System.out.println(s);
		System.out.println("minValue :- " +MIN_VALUE2 + "     "+  "  maxvalue:- "        + MAX_VALUE2   );
		  
		
		
		//integer
		
		int i=127;
		int MIN_VALUE3=Integer.MIN_VALUE;
		int MAX_VALUE3=Integer.MAX_VALUE;
		System.out.println(i);
		System.out.println("minValue :- " +MIN_VALUE3 + "     "+  "  maxvalue:- "        + MAX_VALUE3   );
		  
		
		//long
		
		long l=127;
		long MIN_VALUE4=Long.MIN_VALUE;
		long MAX_VALUE4=Long.MAX_VALUE;
		System.out.println(l);
		System.out.println("minValue :- " +MIN_VALUE4 + "     "+  "  maxvalue:- "        + MAX_VALUE4   );
		  
		
		//float
		

		float f=127.456677f;
		float MIN_VALUE5=Float.MIN_VALUE;
		float MAX_VALUE5=Float.MAX_VALUE;
		System.out.println(f);
		System.out.println("minValue :- " +MIN_VALUE5 + "     "+  "  maxvalue:- "        + MAX_VALUE5   );
		  
		
		//double
		
		double d=127.222222333333334f;
		double MIN_VALUE6=Double.MIN_VALUE;
		double MAX_VALUE6=Double.MAX_VALUE;
		System.out.println(d);
		System.out.println("minValue :- " +MIN_VALUE6 + "     "+  "  maxvalue:- "        + MAX_VALUE6   );
		  
		
		
		char g='A';
		System.out.println(g);
		
		boolean h= false;
		System.out.println(h);
		
	}

}
