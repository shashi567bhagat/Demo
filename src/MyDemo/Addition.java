package MyDemo;



/*
 * Addition ,subtraction,multiplication & division of two no with static keyword
 * */

public class Addition {
	
	public static void main(String[] args) {
		
		
		AdditionOfTwoNumber();
		SubtractionOfTwoNumber();
		MulOfTwoNumber();
		DivOfTwoNumber();
		
	}
	
	public static void AdditionOfTwoNumber()
	{
		
		int number1=4;
		int number2=8;
		
		System.out.println(number1+number2);
		
	}
	
	
	

	public static void SubtractionOfTwoNumber() {
  		
  		int number1=3;
		int number2=8;
		
		System.out.println(number1-number2);
		
  	}
		
  	
	public static void MulOfTwoNumber() {
  		
  		int number1=3;
		int number2=8;
		
		System.out.println(number1*number2);
		
  	}


	public static void DivOfTwoNumber() {
	
	  int number1=3;
     int number2=8;

System.out.println(number1/number2);

}

	
	
	
	
	
}



